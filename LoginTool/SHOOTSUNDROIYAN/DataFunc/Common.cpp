char* GetMapName(int Code)
{
	int i, j;

	for (i=0; i<30; i++)
	{
		if (GameData.Door[i].Status == TRUE)
		{
			for (j=0; j<MAXMAP; j++)
			{
				if ((MapData[j].Site == Code) && (GameData.Door[i].Site == MapData[j].Now))
				{
					return GameData.Door[i].Name;
				}
			}
		}
	}

	return "";
}

BOOL GetDoorStatus(char Name[20])
{
	int i;

	for (i=0; i<30; i++)
	{
		if (strcmp(GameData.Door[i].Name, Name) == 0)
		{
			return TRUE;
		}
	}

	return FALSE;
}

